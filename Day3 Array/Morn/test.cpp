#include<bits/stdc++.h>
using namespace std;
int main(){
    int i,j,x=3,y=3;
    for(i=x;i<x+1;i++){
        for(j=y;j<y+1;j++){
            cout<<"i ="<<i<<endl;
            cout<<"x ="<<x<<endl;
            cout<<"j ="<<j<<endl;
            cout<<"y ="<<y<<endl;
            x+=1;
            y+=1;
        }
    }
}